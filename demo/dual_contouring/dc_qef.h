#pragma once

#include "dc_sdf.h"
#include "dc_hermite.h"
#include "dc_quadtree.h"

#include <vector>
#include <cmath>

namespace dc {

//=============================================================================
// QEF (Quadratic Error Function) 求解器
//=============================================================================
// QEF 原理：
// 在双轮廓线（Dual Contouring）中，我们需要找到一个最佳顶点位置 x，
// 使得该顶点到所有切平面（由法线和位置定义）的距离平方和最小。
//
// 切平面约束：
//   对于每个 Hermite 数据 (p_i, n_i)，顶点 x 满足 n_i · (x - p_i) = 0
//   即 n_i · x = n_i · p_i
//
// QEF 最小化问题：
//   min ||Ax - b||^2
//   其中 A 的行向量是法线 n_i，b_i = n_i · p_i
//
// 求解方法：
//   使用 SVD 分解：A = U Σ V^T
//   最小二乘解：x = V Σ^+ U^T b
//   其中 Σ^+ 是奇异值的伪逆（1/σ_i for σ_i > ε）
//
// 奇异值处理：
//   当矩阵秩不足时（法线共线或几乎共线），添加小量扰动避免数值不稳定
//=============================================================================
class QEFSolver {
public:
    // 构造函数
    QEFSolver() = default;
    
    // 析构函数
    ~QEFSolver() = default;
    
    //=============================================================================
    // 添加约束（切平面）
    // 参数：
    //   normal: 表面法线（指向外部）
    //   point: 表面交点位置
    //=============================================================================
    void AddConstraint(const ImVec2& normal, const ImVec2& point);
    
    //=============================================================================
    // 添加 Hermite 数据作为约束
    // 参数：
    //   data: Hermite 数据（包含位置和法线）
    //=============================================================================
    void AddConstraint(const HermiteData& data);
    
    //=============================================================================
    // 清除所有约束
    //=============================================================================
    void Clear();
    
    //=============================================================================
    // 执行求解
    // 使用 SVD 分解求解最小二乘问题
    // 返回值：
    //   ImVec2: 最小化 QEF 的顶点位置
    //=============================================================================
    ImVec2 Solve();
    
    //=============================================================================
    // 获取解算结果（与 Solve() 等效，兼容 API）
    //=============================================================================
    ImVec2 GetSolution() { return Solve(); }
    
    //=============================================================================
    // 获取误差
    // 计算 ||Ax - b||^2，即 QEF 的最小值
    // 返回值：
    //   float: 误差值
    //=============================================================================
    float GetError();
    
    //=============================================================================
    // 获取约束数量
    //=============================================================================
    int GetConstraintCount() const { return static_cast<int>(m_normals.size()); }
    
    //=============================================================================
    // 检查是否有足够的约束
    // 至少需要 2 个不共线的约束才能求解
    //=============================================================================
    bool HasValidConstraints() const;
    
private:
    std::vector<ImVec2> m_normals;  // 法线数组
    std::vector<ImVec2> m_points;  // 位置数组
    
    //=============================================================================
    // 处理奇异矩阵
    // 当矩阵接近奇异时，添加小量扰动使解稳定
    //=============================================================================
    ImVec2 SolveSingularCase(const std::vector<ImVec2>& normals, const ImVec2& b);
    
    //=============================================================================
    // 计算点乘
    //=============================================================================
    static float Dot(const ImVec2& a, const ImVec2& b) { return a.x * b.x + a.y * b.y; }
};

} // namespace dc