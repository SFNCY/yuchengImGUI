#pragma once

#include "dc_mesh.h"
#include "dc_sdf.h"

#include "imgui.h"

#include <vector>

// ============================================================================
// 可视化阶段枚举
// ============================================================================
/// @brief 可视化阶段类型
/// @details 用于分步可视化系统的阶段切换
enum class VisStage {
    TREE_BUILD,     ///< 阶段1：四叉树构建
    QEF_SOLVE,      ///< 阶段2：QEF 求解
    MESH_GENERATE,  ///< 阶段3：网格生成
    ALL             ///< 全部阶段（同时显示）
};

// ============================================================================
// ShapeType - 形状类型枚举
// ============================================================================
/// @brief 支持的形状类型
enum class ShapeType {
    Circle,     ///< 圆形 SDF
    Square     ///< 方形 SDF（轴对齐）
};

// ============================================================================
// ShapeInfo - 形状信息结构体
// ============================================================================
/// @brief 存储形状的类型、位置和大小参数
struct ShapeInfo {
    ShapeType type;     ///< 形状类型
    ImVec2    position; ///< 形状中心位置
    float     size;     ///< 大小参数（圆形为半径，方形为半边长）

    ShapeInfo(ShapeType t, ImVec2 pos, float s) : type(t), position(pos), size(s) {}
};

// ============================================================================
// AppState - 应用程序状态结构体
// ============================================================================
// 
// 该结构体存储 Dual Contouring 演示程序的所有可配置参数。
//
// 用途：
// - 作为 ImGui 控制面板的数据绑定目标
// - 在渲染循环中传递配置参数
// - 控制 SDF 参数、可视化选项和渲染行为
//
// 使用方法：
// @code
//   AppState state;
//   state.maxDepth = 4;
//   state.qefThreshold = 0.1f;
//   
//   // 在 ImGui 渲染时
//   RenderControlPanel(&state);
//   
//   // 在渲染场景时使用这些参数
//   BuildQuadtree(sdf, state.maxDepth, state.qefThreshold);
// @endcode
// ============================================================================
struct AppState {
    // ========================================================================
    // 算法参数
    // ========================================================================
    
    /// @brief 四叉树最大深度
    /// @details 控制 SDF 评估的精细程度
    /// - 深度 1: 最粗糙，只有根节点
    /// - 深度 4: 默认值，平衡质量和性能
    /// - 深度 8: 最精细，但计算量大
    /// @note 有效范围: [1, 8]
    int maxDepth = 4;
    
    /// @brief QEF (Quadratic Error Function) 阈值
    /// @details 用于判断顶点是否足够"好"
    /// - 较小的值: 更严格的拟合，但可能产生更多顶点
    /// - 较大的值: 更快的收敛，但可能丢失细节
    /// @note 有效范围: [0.001f, 1.0f]
    float qefThreshold = 0.1f;
    
    // ========================================================================
    // 可视化选项
    // ========================================================================
    
    /// @brief 是否显示 SDF 等值线
    /// @details 使用 Marching Squares 算法绘制 SDF=0 的轮廓
    bool showSDFContour = true;
    
    /// @brief 是否显示四叉树边界
    /// @details 绘制所有叶子节点的边界框
    bool showQuadtreeBounds = true;
    
    /// @brief 是否显示四叉树节点中心点
    /// @details 在每个节点中心绘制一个小圆点
    bool showQuadtreeNodes = false;
    
    /// @brief 是否显示对偶网格
    /// @details 绘制顶点和边，形成对偶网格
    bool showDualMesh = true;
    
    /// @brief 是否显示 QEF 交点
    /// @details 绘制法线与单元边界的交点（调试用）
    bool showQEFIntersections = false;
    
    /// @brief 是否显示法线向量
    /// @details 在每个叶子节点顶点处显示法线方向
    bool showNormals = false;
    
    // ========================================================================
    // 统计信息（只读，由应用程序更新）
    // ========================================================================
    
    /// @brief 当前 SDF 评估次数（调试用）
    int sdfEvalCount = 0;
    
    /// @brief 四叉树节点总数
    int quadtreeNodeCount = 0;
    
    /// @brief 对偶网格顶点数
    int meshVertexCount = 0;
    
    /// @brief 对偶网格边数
    int meshEdgeCount = 0;
    
    // ========================================================================
    // 分步可视化控制
    // ========================================================================
    
    /// @brief 当前可视化阶段
    /// @details 用于分步展示算法过程
    VisStage visStage = VisStage::ALL;
    
    /// @brief 是否启用分步模式
    /// @details 启用后可以逐步查看算法各阶段
    bool stepMode = false;
    
    /// @brief 当前步数
    /// @details 在分步模式下跟踪当前进度
    int currentStep = 0;
    
    /// @brief 自动播放模式
    /// @details 启用后自动逐步推进
    bool autoPlay = false;
    
    /// @brief 自动播放间隔（毫秒）
    /// @details 控制自动播放速度
    int autoPlayInterval = 500;
    
    // ========================================================================
    // 形状编辑器相关
    // ========================================================================
    
    /// @brief 形状列表
    /// @details 存储所有已添加的形状，用于构建复合 SDF
    std::vector<ShapeInfo> shapes;
    
    /// @brief 当前选中的形状索引
    /// @details -1 表示没有选中任何形状
    int selectedShape = -1;
    
    /// @brief 形状编辑器是否可见
    bool showShapeEditor = true;
    
    /// @brief 获取形状数量
    int GetShapeCount() const { return (int)shapes.size(); }
    
    /// @brief 检查是否有形状
    bool HasShapes() const { return !shapes.empty(); }
    
    // ========================================================================
    // UI 布局相关
    // ========================================================================
    
    /// @brief 当前选中的标签页索引
    /// @details 0=算法参数，1=可视化选项，2=形状编辑器，3=统计信息
    int currentTabIndex = 0;
    
    /// @brief 分割线位置
    /// @details 控制左右面板的宽度
    float splitterPosition = 320.0f;
    
    /// @brief 折叠前记录的分割线位置
    /// @details 用于恢复折叠前的布局
    float lastSplitterPosition = 320.0f;
    
    /// @brief 参数面板是否折叠
    /// @details 折叠后只显示标题栏
    bool parameterPanelCollapsed = false;
};

// ============================================================================
// RenderControlPanel - 渲染控制面板
// ============================================================================
//
// 使用 ImGui 控件渲染一个左侧/右侧控制面板，允许用户调整算法参数和可视化选项。
//
// 面板内容：
// - 算法参数区域：最大深度、QEF 阈值
// - 可视化选项区域：各种可视化开关
// - 统计信息区域：（只读）显示当前状态
//
// 参数：
//   @param state 应用程序状态指针（可为 nullptr，但建议传入有效指针）
//
// 示例：
// @code
//   AppState state;
//   RenderControlPanel(&state);
// @endcode
// ============================================================================
void RenderControlPanel(AppState* state);

// ============================================================================
// RenderInfoPanel - 渲染信息面板
// ============================================================================
//
// 显示网格统计信息和性能指标。
//
// 显示内容：
// - 对偶网格统计：顶点数、边数
// - 四叉树统计：节点数、深度
// - 帧率信息：FPS、帧时间
//
// 参数：
//   @param state 应用程序状态指针
//   @param mesh 对偶网格指针（可为 nullptr）
//
// 示例：
// @code
//   RenderInfoPanel(&state, &mesh);
// @endcode
// ============================================================================
void RenderInfoPanel(AppState* state, const dc::DualMesh* mesh);

// ============================================================================
// UpdateStatsFromMesh - 从对偶网格更新统计信息
// ============================================================================
//
// 从对偶网格提取统计信息并更新到 AppState。
//
// 参数：
//   @param state 应用程序状态指针（更新此结构体）
//   @param mesh 对偶网格指针（读取此结构体）
// ============================================================================
void UpdateStatsFromMesh(AppState* state, const dc::DualMesh* mesh);

// ============================================================================
// 分步可视化渲染函数
// ============================================================================

// ============================================================================
// RenderTreeStructure - 可视化四叉树结构
// ============================================================================
//
// 使用线条绘制四叉树节点边界框，递归遍历所有节点。
//
// 绘制内容：
// - 每个节点用一个正方形表示其边界
// - 叶子节点用实线，内部节点用虚线
// - 不同深度使用不同颜色区分
//
// 参数：
//   @param root 四叉树根节点指针（可为 nullptr）
//   @param renderer 渲染器指针（用于绘制）
// ============================================================================
void RenderTreeStructure(const dc::QuadtreeNode* root, Renderer* renderer);

// ============================================================================
// RenderQFESolving - 可视化 QEF 求解过程
// ============================================================================
//
// 显示 Hermite 交点（法线与边界的交点）和 QEF 解算点。
//
// 绘制内容：
// - 绿色圆点：Hermite 交点（法线与边界的交点）
// - 蓝色圆点：QEF 解算点（顶点位置）
// - 黄色线条：连接交点到解算点的向量
//
// 参数：
//   @param root 四叉树根节点指针（可为 nullptr）
//   @param renderer 渲染器指针
// ============================================================================
void RenderQFESolving(const dc::QuadtreeNode* root, Renderer* renderer);

// ============================================================================
// RenderAllStages - 根据当前阶段渲染
// ============================================================================
//
// 根据 AppState 中的 visStage 和 stepMode 设置，
// 渲染相应的可视化内容。
//
// 渲染逻辑：
// - stepMode = false：直接渲染 visStage 指定的阶段
// - stepMode = true：根据 currentStep 决定渲染内容
//   - Step 0: 只渲染 TREE_BUILD
//   - Step 1: 渲染 TREE_BUILD + QEF_SOLVE
//   - Step 2: 渲染全部阶段
//
// 参数：
//   @param state 应用程序状态指针
//   @param renderer 渲染器指针
//   @param root 四叉树根节点指针
// ============================================================================
void RenderAllStages(const AppState* state, Renderer* renderer, const dc::QuadtreeNode* root);

// ============================================================================
// RenderStepControls - 渲染分步控制按钮
// ============================================================================
//
// 渲染分步可视化的控制按钮，包括：
// - 模式切换：连续/分步
// - 步进控制：上一步/下一步
// - 自动播放：播放/暂停
// - 进度指示器
//
// 参数：
//   @param state 应用程序状态指针
// ============================================================================
void RenderStepControls(AppState* state);

// ============================================================================
// 形状编辑器函数
// ============================================================================

// ============================================================================
// AddCircle - 添加圆形形状
// ============================================================================
/// @brief 在指定位置添加一个新圆形到形状列表
/// @param state 应用程序状态指针
/// @param pos 圆形中心位置
/// @param radius 圆形半径
void AddCircle(AppState* state, ImVec2 pos, float radius);

// ============================================================================
// AddSquare - 添加方形形状
// ============================================================================
/// @brief 在指定位置添加一个新方形到形状列表
/// @param state 应用程序状态指针
/// @param pos 方形中心位置
/// @param size 方形边长
void AddSquare(AppState* state, ImVec2 pos, float size);

// ============================================================================
// RemoveShape - 删除指定形状
// ============================================================================
/// @brief 从形状列表中删除指定索引的形状
/// @param state 应用程序状态指针
/// @param index 要删除的形状索引（从 0 开始）
void RemoveShape(AppState* state, int index);

// ============================================================================
// RenderShapeEditor - 渲染形状编辑器面板
// ============================================================================
/// @brief 使用 ImGui 渲染形状编辑界面
/// @details 显示形状列表，支持选择、编辑、添加和删除形状
/// @param state 应用程序状态指针
void RenderShapeEditor(AppState* state);

// ============================================================================
// RenderSplitter - 渲染可拖动分割线
// ============================================================================
/// @brief 在指定位置渲染一个可拖动的垂直分割线
/// @param splitterX 分割线 X 坐标（输入输出）
/// @param minWidth 左侧区域最小宽度
/// @param maxWidth 左侧区域最大宽度
/// @param totalWidth 总宽度
/// @param splitterThickness 分割线厚度（像素）
/// @param is_dragging 拖动状态指针（外部管理，避免静态状态污染）
void RenderSplitter(float* splitterX, float minWidth, float maxWidth, float totalWidth, float splitterThickness, bool* is_dragging);

// ============================================================================
// GetCombinedSDF - 获取复合 SDF
// ============================================================================
/// @brief 遍历所有形状，构建并返回它们的复合 SDF（并集操作）
/// @param state 应用程序状态指针
/// @return 指向复合 SDF 的指针（静态存储）
const SDFBase* GetCombinedSDF(AppState* state);
